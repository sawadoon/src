package basic1.lesson1;

/**
 * Lesson1_03 変数 「2 値の上書き」
 * 
 * 変数の値は上書きすることができる。
 * 
 */

public class Lesson1_03 {
    
    public static void main(String[] args) {

        // (記)int型の変数num を宣言して「123」を代入してください。
        int num = 123;

        // (記)変数numの値を、コンソールに出力してください。
        System.out.println(num);

        // (記)変数num を「456」に上書きしてください。
        num = 456;

        // (記)もう一度、変数num の値を、コンソールに出力してください。
        System.out.println(num);


        // (記)String型の変数str を宣言して「"1"」を代入してください。
        String str = "1";

        // (記)変数str の値を、コンソールに出力してください。
        System.out.println(str);

        // (記)変数str を「"2"」に上書きしてください。
        str = "2";

        // (記)もう一度、変数str の値を、コンソールに出力してください。
        System.out.println(str);


        // (記)boolean型の変数flg を宣言して「false」を代入してください。
        boolean flg = false;

        // (記)変数flg の値を、コンソールに出力してください。
        System.out.println(flg);

        // (記)変数flg を「true」に上書きしてください。
        flg = true;

        // (記)もう一度、変数flg の値を、コンソールに出力してください。
        System.out.println(flg);
    }
}
